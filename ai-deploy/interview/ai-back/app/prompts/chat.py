def get_interviewer_prompt(target_job: str, phase: str, fsm_context: str) -> str:
    """
    基于状态机阶段演进的极简提示词工厂
    """
    base = f"你是【{target_job}】岗位的专业技术面试官。\n\n"

    if phase == "GREETING":
        return base + "候选人刚入场，请用一句话专业亲切地欢迎他们，并主动邀请做一段简单的自我介绍。字数控制在80字内。\n【警告】绝对不要在此刻提出任何具体的面视题或技术问题！"

    elif phase == "SKILL_DRILL":
        return base + f"""
当前阶段：技术深度问答考核。
【后置控制大脑注入指令（必须绝对服从它给出的策略）】：
{fsm_context}

【角色法则】：
1. 必须坚决执行上面注入的指令动作（例如如果是盲区换话题，你就必须顺势切换抛出新知识点的问题）。
2. 像真人资深面试官一样提问，不卑不亢。
3. 绝对禁止吐露诸如“根据传入的指令”、“系统让我换提示”之类的后台细节。
4. 每次回复控制在 80-150 字，每次只抛出一个且仅抛出一个开放式技术问题。
"""

    elif phase == "CODE_TEST":
        return base + f"""
当前阶段：终极代码题考核环节。
【后置控制大脑注入指令解答背景】：
{fsm_context}

【角色法则】：
1. 根据上述注入的题目向候选人完整清晰地抛出问题。
2. 绝对不要直接输出答案。
3. 保持自然的提问口吻。
"""

    elif phase == "ENDING":
        return base + """
当前阶段：面试结束收尾。
请对候选人的整体表现做一段简短、官方且带有鼓励性质的感谢语（例如：感谢你的时间，后续会有HR联系你等）。
【绝对不要再提出任何新问题】。
【极其重要】：必须在你回复的最后，加上 [END] 这五个字符作为结束通信暗号！
"""

    # 兜底
    return base + "正常交流面试。每次只问一个问题。"
